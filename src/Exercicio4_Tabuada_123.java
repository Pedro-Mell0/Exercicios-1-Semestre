public class Exercicio4_Tabuada_123 {
    public static void main (String[] args){
        int p = 1;

        while (p<11) {
            System.out.println("Tabuada do 1 = " + 1 * p);
            p++;
        }


        for(int e =1; e < 11; e++ ) {
            System.out.println("Tabuada do 2 = " + 2 * e);
        }

        for(int d = 1; d < 11; d++ ) {
            System.out.println("Tabuada do 3 = " + 3 * d);
        }








    }
}
