package ExerciciosAula3;

public class Exercicio1While_Do_For {
    public static void main (String[] args) {
    int i = 1;

    while(i<11){
        System.out.println("i = " + i);
        i++;
    }




int j = 1;
    do{
        System.out.println("j = " + j);
        j++;

    }
    while(j<10);
            System.out.println("j = " + j);
            j++;







    for(int k =0; k < 11; k++) {
        System.out.println("k = " + k);

    }




    }

}


