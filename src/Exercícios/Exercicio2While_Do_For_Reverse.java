public class Exercicio2While_Do_For_Reverse {
    public static void main(String[] args){
        int i = 10;

        while(i>-1){
            System.out.println("i = " + i);
            i--;
        }




        int j = 10;
        do{
            System.out.println("j = " + j);
            j--;

        }
        while(j>0);
        System.out.println("j = " + j);
        j++;







        for(int k =10; k > -1; k--) {
            System.out.println("k = " + k);

        }
    }
}
